/* Mux helper macros */
#define IMXRT_IOMUX_ALT(x)            (x)
#define IMXRT_IOMUX_SION              BIT(4)

/* Pad helper macros */

/* Slew rate: 0 - Slow, 1 - Fast */
#define IMXRT_IOMUX_SRE               BIT(0)

/* Drive strength
 *
 * DSE      DS@3.3V    DS@1.8V
 * ---      -------    -------
 *
 * 0      Output driver disabled
 * 1        150 Ohm    260 Ohm
 * 2        75 Ohm     130 Ohm
 * 3        50 Ohm     87 Ohm
 * 4        38 Ohm     65 Ohm
 * 5        30 Ohm     52 Ohm
 * 6        25 Ohm     43 Ohm
 * 7        21 Ohm     37 Ohm
 */
#define IMXRT_IOMUX_DSE(x)            ((x & 7) << 3)

/* Speed
 *
 * 0 - Low, 50 MHz
 * 1 - Medium, 100 MHz
 * 2 - Fast, 150 MHz
 * 3 - Max, 200 MHz
 */
#define IMXRT_IOMUX_SPEED(x)          ((x & 3) << 6)

/* Open Drain Enable */
#define IMXRT_IOMUX_ODE               BIT(11)

/* Pull / Keep Enable
 *
 * 0 - Pull / Keep disabled
 * 1 - Pull / Keep enabled
 */
#define IMXRT_IOMUX_PKE               BIT(12)

/* Pull / Keep Select
 *
 * 0 - Keeper
 * 1 - Pull
 */
#define IMXRT_IOMUX_PUE               BIT(13)

/* Pull Up / Down Config
 * 0 - 100 kOhm Pull Down
 * 1 - 47 kOhm Pull Up
 * 2 - 100 kOhm Pull Up
 * 3 - 22 kOhm Pull Up
 */
#define IMXRT_IOMUX_PUS(x)            ((x & 3) << 14)

/* Hysteresis enable */
#define IMXRT_IOMUX_HYS               BIT(16)

